package entity;

/**
 *
 * @author Admin
 */
public class Item {
    private int itemCode;
    private String name;
    private float price;

    public Item() {
    }

    public Item(int itemCode, String name, float price) {
        this.itemCode = itemCode;
        this.name = name;
        this.price = price;
    }

    public int getItemCode() {
        return itemCode;
    }

    public void setItemCode(int itemCode) {
        this.itemCode = itemCode;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
    
    
}
