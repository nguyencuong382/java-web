package entity;

import java.util.Date;

/**
 *
 * @author Admin
 */
public class Student {
    private int id;
    private String name;
    private int classId;
    private boolean gender;
    private Date DOB;

    public Student() {
    }

    public Student(int id, String name, int classId, boolean gender, Date DOB) {
        this.id = id;
        this.name = name;
        this.classId = classId;
        this.gender = gender;
        this.DOB = DOB;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getClassId() {
        return classId;
    }

    public void setClassId(int classId) {
        this.classId = classId;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public Date getDOB() {
        return DOB;
    }

    public void setDOB(Date DOB) {
        this.DOB = DOB;
    }
    
}
